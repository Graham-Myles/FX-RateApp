// app.module.js

angular.module('app', ['ngRoute', 'ngAnimate', 'ngSanitize'])
  .config(function($routeProvider) {
    $routeProvider
      .when('/', {
        templateUrl: 'views/main.html',
        controller: 'MainCtrl'
      })
      .otherwise({
        redirectTo: '/'
      });
  });
